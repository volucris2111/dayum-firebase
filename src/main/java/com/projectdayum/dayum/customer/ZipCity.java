package com.projectdayum.dayum.customer;

public class ZipCity {
	private String city;
	private String zip;
	
	public String getCity() {
		return this.city;
	}
	
	public String getZip() {
		return this.zip;
	}
	
	public void setCity(String city) {
		this.city = city;
	}
	
	public void setZip(String zip) {
		this.zip = zip;
	}
}
